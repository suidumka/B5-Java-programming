package day07_relational_operators;

public class AllOperators {
    public static void main(String[] args) {
        System.out.println(5 + 2 * 4);
        // 5 + 8
        // 13

        System.out.println(5 * 2 + 6 / 2);
        // 10 + 3
        // 13

        System.out.println(5 + 2 > 4 + 6);
        // 7 > 10
        // false
    }
}
