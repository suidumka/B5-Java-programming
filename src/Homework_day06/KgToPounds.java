package Homework_day06;

public class KgToPounds {
    public static void main(String[] args) {
        double kg = 10;
        double pound = 22.0462;
        double pound_to_kg = pound / kg;

        double kg1 = 202;
        double kg_to_pound = kg1 * pound_to_kg;

        System.out.println(kg1 + " is equal to " + kg_to_pound + " pounds");
    }
}
